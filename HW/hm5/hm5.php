1. Что такое <ol>, <ul> и <li>?
    Тег <ol> устанавливает нумерованный список. Каждый элемент списка должен начинаться с тега <li>.
    Если к тегу <ol> применяется таблица стилей, то элементы <li> наследуют эти свойства.
    Тег <ul> устанавливает ненумированный (маркированный) список.


