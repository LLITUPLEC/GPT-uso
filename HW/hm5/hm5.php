10. Перечислить по пять знакомых вам псевдоклассов и псевдостилей с указанием их функционала.
                                :link – отвечает за стили непосещенной ссылки;
                                :visited – состояние посещенной ссылки;
                                :active – состояние активного объекта;
                                :hover – состояние объекта при наведении на него мышкой;
                                :focus – фокус на объекте;
                                :first-child – первый дочерний элемент текущего элемента;
                                :last-child – последний дочерний элемент;
    Псевдоклассы – это то, что обычно пишут после селектора через двоеточие чтобы определить
    реакцию или состояние для данного селектора. Самые известные псевдоклассы это :link, :hover,
    :visited и :active. Псевдоклассы обладают следующей структурой: selector:pseudo class { property:
    value; }, т.е. нужно всего лишь поместить двоеточие между селектором и псевдоклассом.
                                a.basket:link {
                                   color: blue;
                                }
                                a.basket:visited {
                                   color: purple;
                                }
                                a.basket:active {
                                   color: red;
                                }
                                a.basket:hover {
                                  text-decoration: none;
                                  color: blue;
                                  background-color: yellow;
                                }



